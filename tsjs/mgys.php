    <p>水光动能现象和一般牙钻不同之处在于：作用于硬组织时，不会出现振动，也不会产生 热量，能使患者神经不会感觉过分敏感而产生疼痛。由于激光 能量直接作用于软组并有恰当的吸水率，因此可获得极佳的切割和止血效果，创面还不会产生结痂区。资料表明，90%患者在接受治疗时无疼痛感，所以无需麻 醉。</p>
    <p>因为水激光的靶组织是水。治疗系统通过三条管线（光纤、水管、气管 ）分别输出激光、水滴和气流。在距手柄末端1-2mm的范围内，数以千亿的水分子强烈吸收激光赋予的能量，从而变得非常不稳定，这些不稳定水分子在几个纳 米的范围内发生微爆破撞向牙齿组织从而使光能转变为机械能，达到破坏靶组织的目。同时，水激光还可通过调节水汽比例及能量参数，使手术部位产生一种类似于 吗啡样电生物刺激效应，从而阻断神经传导，达到镇痛的目的。</p>
    <p>更重要的是，水激光波长近红外线光，完全不会产生基因突变，所以安全性极高。另外，它还不会造成牙体折裂。同时，由于表面不会留下污染层，所以使修复后的牙体的硬度比涡轮牙科治疗后的硬度提高80%，与原来牙体结合部的粘合力提高50%。</p>
    <p>水激光技术适应范围：</p>
    <p>1.缺失一颗牙，特别是缺失前牙，而不愿意磨损两边的牙齿。</p>
    <p>2.缺失多个牙或者全部牙，使用传统假牙有困难、功能不好，带不好的患者。</p>
    <p>3.对假牙的美观、功能有特殊要求者。</p>
    <p>4.缺牙周围的余牙有问题，不能做传统假牙的患者。</p>
    <p>5.没有严重牙周炎、牙周病及其他严重口腔疾病。</p>
    <p>6.上下牙之间的间隙咬合时大于0.5厘米。</p>
    <p>水激光禁忌范围：糖尿病、肾病、心脏病、凝血障碍疾病、精神 障碍、严重骨质疏松、传染及骨代谢疾病等没有结束骨颈部放射线治疗期。</p>



