          <p >
 
            微创种植牙（Minimally Invasive Dental Implant）是 将微创技术应用到了种植牙领域，在传统种植牙的基础上用特殊的手术方式和技术（Graftless solution）来完成种植牙的植入过程，利用微小切口（3～4毫米）将人工牙根“种”入口腔内，摆脱了传统种植牙切开翻起牙肉、缝合、拆线等步骤，将 创伤降低到最小程度，大大减少了肿痛和出血量，大幅度缩短了种植过程所需的时间（种植过程仅需15-30分钟），而且减少了感染的风险,术后几乎不会疼痛 和肿胀,是国际上广泛应用的种植方式
          </p>


          <p style="text-align: center;">
            <span style="font-size: 18px;">
              <img src="../images/tsjs/2-4lp.jpg"  alt="美国3MLava全瓷牙" >
