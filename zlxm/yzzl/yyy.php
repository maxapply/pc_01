                    牙龈出血是牙周疾病和全身疾病在牙龈组织上常发生的一个症状.牙周疾病有牙龈炎、牙周炎、走马牙疳、牙龈毛细血管瘤、牙龈癌等;全身性疾病有各种血液病、 与内分泌有关的一些妇科病,较少见的还有维生素c缺乏症、遗传性毛细血管扩张症、高热性疾病等.全身性牙龈出血应针对有关疾病的病因加以治疗,下面只介绍 由局部原因引起的牙龈出血的简易疗法：
    <br>
                  牙龈出血怎么办一
                  <p>应针对不同的原因加以防治如牙龈炎、牙周病等的牙龈出血,只要注意口腔卫生,勤于刷牙漱口,除去牙结石和不良假牙嵌补物的刺激,消除菌斑和修缮或重新调整不适合的假牙,固定松动的牙齿,调整各个牙齿的咬颌关系,就可治愈。</p>
            
                  牙龈出血怎么办二
                  <p>牙龈出血常伴有口臭,有碍病人工作社交活动,有的会给病人带来精神负担.养成良好的口腔卫生习惯,可以预防牙龈炎、牙周炎的发生.坚持早晚刷牙,饭后漱口,清除污物和食物残渣,可防止牙垢和牙结石的形成.定期到医院进行牙周洁治(洗牙)是好的牙齿保健方法。</p>
                  牙龈出血怎么办三

                  <p>牙龈出血与维生素c缺乏有一定关系,其实不然.科学研究表明,牙周炎程度与血中维生素c的浓度之间无明显关系,牙周炎患者单纯补充维生素c并不能改善病情,况且患者通常都是摄取平衡饮食者。</p>

